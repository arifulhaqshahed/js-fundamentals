// remove element from an array by using pop
var books = ['genetics', 'microbiology', 'chemistry', 'biotechnology', 'cellbiology'];
books.pop();

// remove element from the beggining of the array by shift()
books.shift();

// add element in the beggining of the array by unshift()
books.unshift('bangla');
console.log(books)